export function getHostBySlug(slug: string): HostingProvider | undefined {
  return hosts.find((h) => h.slug === slug);
}

export function getTopHosts(count: number = 3): HostingProvider[] {
  return [...hosts].sort((a, b) => b.rating - a.rating).slice(0, count);
}

export function getHostsByCategory(category: string): HostingProvider[] {
  return hosts.filter((h) =>
    h.bestFor.some((tag) => tag.toLowerCase().includes(category.toLowerCase()))
  );
}

